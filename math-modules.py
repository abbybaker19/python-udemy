# modules = a bunch of prewritten code

import math

# returns ceiling rounded to nearest whole number
print("ceil(4.4) =", math.ceil(4.4))

# returns floor rounded to nearest whole number
print("floor(4.4) =", math.floor(4.4))

# returns absolute value
print("fabs(-4.4) =", math.fabs(-4.4))

# returns factorial
print("factorial(4) =", math.factorial(4))

# returns remainder of division
print("fmod(5, 4) =", math.fmod(5, 4))

# returns x^y
print("pow(2, 2) =", math.pow(2, 2))

# returns square root
print("sqrt(4) =", math.sqrt(4))

# special values
print("math.e =", math.e)
print("math.pi =", math.pi)

# define base and 10^3
print("log(1000, 10) =", round(math.log(1000, 10), 3))

# can also use base 10 like this
print("log10(1000) =", math.log10(1000))

# trig functions: sin, cos, tan, asin, acos, atan, atan2, asinh,
# acosh, atanh, sinh, cosh, tanh
# follow the following format:
print("sin(0) =", math.sin(0))

# convert radians to degrees and vice versa
print("degrees(1.5708) =", math.degrees(1.5708))
print("radians(90) =", math.radians(90))
